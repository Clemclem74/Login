package buisnessLogic;

public class Contact {

	private int idContact;
	private String nameContact;
	private String company;
	private String phoneNumberContact;
	private String informationsContact;
	private Team teamContact;
	
	
	
	public int getIdContact() {
		return idContact;
	}
	
	public void setIdContact(int idContact) {
		this.idContact = idContact;
	}
	
	public String getNameContact() {
		return nameContact;
	}
	public void setNameContact(String nameContact) {
		this.nameContact = nameContact;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getPhoneNumberContact() {
		return phoneNumberContact;
	}
	public void setPhoneNumberContact(String phoneNumberContact) {
		this.phoneNumberContact = phoneNumberContact;
	}
	public String getInformationsContact() {
		return informationsContact;
	}
	public void setInformationsContact(String informationsContact) {
		this.informationsContact = informationsContact;
	}

	public Team getTeamContact() {
		return teamContact;
	}

	public void setTeamContact(Team teamContact) {
		this.teamContact = teamContact;
	}


	
	
	
}